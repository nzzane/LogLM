-- Backup log — tracks pg_dump + redis RDB snapshots taken by scripts/backup.py
-- ═══════════════════════════════════════════════════════════════════════════
CREATE TABLE IF NOT EXISTS backup_log (
    id          BIGSERIAL PRIMARY KEY,
    timestamp   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    kind        TEXT NOT NULL,           -- postgres | redis | full
    destination TEXT NOT NULL,
    size_bytes  BIGINT,
    duration_s  REAL,
    status      TEXT NOT NULL,           -- ok | error
    detail      TEXT
);
CREATE INDEX IF NOT EXISTS idx_backup_log_ts ON backup_log (timestamp DESC);


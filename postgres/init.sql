-- Drop old partitions by retention (called from processor retention loop).
-- Drops entire partitions whose end bound is older than cutoff_days, which
-- is instant compared to row-by-row DELETE.
CREATE OR REPLACE FUNCTION drop_old_partitions(
    target_table TEXT,
    cutoff_days INT
) RETURNS INT AS $$
DECLARE
    r          RECORD;
    dropped    INT := 0;
    cutoff     TIMESTAMPTZ;
BEGIN
    cutoff := NOW() - (cutoff_days || ' days')::INTERVAL;
    FOR r IN
        SELECT c.relname AS part_name,
               pg_get_expr(c.relpartbound, c.oid) AS bound_expr
        FROM pg_inherits i
        JOIN pg_class c ON c.oid = i.inhrelid
        JOIN pg_class p ON p.oid = i.inhparent
        WHERE p.relname = target_table
        ORDER BY c.relname
    LOOP
        -- Extract the TO bound from "FOR VALUES FROM (...) TO (...)"
        -- and drop if the entire partition is before the cutoff.
        IF r.bound_expr ~ 'TO \(' THEN
            DECLARE
                to_val TEXT;
                to_ts  TIMESTAMPTZ;
            BEGIN
                to_val := SUBSTRING(r.bound_expr FROM 'TO \(''([^'']+)''\)');
                IF to_val IS NOT NULL THEN
                    to_ts := to_val::TIMESTAMPTZ;
                    IF to_ts <= cutoff THEN
                        EXECUTE FORMAT('DROP TABLE %I', r.part_name);
                        dropped := dropped + 1;
                    END IF;
                END IF;
            EXCEPTION WHEN OTHERS THEN
                NULL; -- skip unparseable partition bounds
            END;
        END IF;
    END LOOP;
    RETURN dropped;
END;
$$ LANGUAGE plpgsql;

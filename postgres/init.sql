-- ═══════════════════════════════════════════════════════════════════════════
-- Embedding cache — semantic search + RAG over past events
-- ═══════════════════════════════════════════════════════════════════════════
-- Stores one row per unique message signature. Vectors are either pgvector's
-- VECTOR type (when the extension is available) or BYTEA (fallback). The
-- analyzer's vector_cache module handles either.
DO $emb$
BEGIN
    IF EXISTS (SELECT 1 FROM pg_extension WHERE extname = 'vector') THEN
        CREATE TABLE IF NOT EXISTS event_embeddings (
            id            BIGSERIAL PRIMARY KEY,
            signature     TEXT NOT NULL UNIQUE,
            sample        TEXT NOT NULL,
            embedding     VECTOR(768),     -- nomic-embed-text dim
            verdict       TEXT,             -- last-known user verdict for this sig
            hit_count     BIGINT NOT NULL DEFAULT 1,
            updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
        );
        BEGIN
            CREATE INDEX IF NOT EXISTS idx_event_emb_ivfflat
                ON event_embeddings USING ivfflat (embedding vector_cosine_ops) WITH (lists = 100);
        EXCEPTION WHEN OTHERS THEN
            -- ivfflat needs ANALYZE data; the cold index can be added later.
            NULL;
        END;
    ELSE
        CREATE TABLE IF NOT EXISTS event_embeddings (
            id            BIGSERIAL PRIMARY KEY,
            signature     TEXT NOT NULL UNIQUE,
            sample        TEXT NOT NULL,
            embedding     BYTEA,
            verdict       TEXT,
            hit_count     BIGINT NOT NULL DEFAULT 1,
            updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
        );
    END IF;
END $emb$;


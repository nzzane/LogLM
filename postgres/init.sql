-- LogLM schema
--
-- Extensions:
--   pgcrypto  – gen_random_uuid() for chat sessions + secure token digests
--   pg_trgm   – fast ILIKE search on message / host columns
--   vector    – pgvector for embedding-based RAG. Optional: CREATE EXTENSION
--               fails silently on a base postgres image; LogLM falls back to
--               BYTEA storage when the extension is absent.
CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE EXTENSION IF NOT EXISTS pg_trgm;
DO $ext$ BEGIN
    BEGIN
        CREATE EXTENSION IF NOT EXISTS vector;
    EXCEPTION WHEN OTHERS THEN
        RAISE NOTICE 'pgvector not available, embedding cache will use BYTEA fallback';
    END;
END $ext$;

-- ═══════════════════════════════════════════════════════════════════════════
-- Raw parsed events — range-partitioned by month on timestamp.
-- Benefits: instant DROP of old months (vs slow DELETE), per-partition
-- vacuuming, partition pruning on all time-range queries.
-- ═══════════════════════════════════════════════════════════════════════════
CREATE SEQUENCE IF NOT EXISTS events_id_seq;

CREATE TABLE IF NOT EXISTS events (
    id          BIGINT NOT NULL DEFAULT nextval('events_id_seq'),
    timestamp   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    host        TEXT NOT NULL,
    source      TEXT NOT NULL,        -- syslog / nginx / firewall / snmp / librenms
    severity    TEXT NOT NULL,
    program     TEXT,
    message     TEXT NOT NULL,
    structured  JSONB DEFAULT '{}',
    verdict     TEXT NOT NULL,        -- keep / store
    event_hash  BYTEA                 -- xxhash128 for idempotent inserts + signature lookup
) PARTITION BY RANGE (timestamp);

ALTER SEQUENCE events_id_seq OWNED BY events.id;

CREATE INDEX IF NOT EXISTS idx_events_ts_brin       ON events USING BRIN (timestamp) WITH (pages_per_range = 32);
CREATE INDEX IF NOT EXISTS idx_events_host          ON events (host);
CREATE INDEX IF NOT EXISTS idx_events_host_ts       ON events (host, timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_events_structured    ON events USING GIN (structured jsonb_path_ops);
CREATE INDEX IF NOT EXISTS idx_events_msg_trgm      ON events USING GIN (message gin_trgm_ops);
CREATE UNIQUE INDEX IF NOT EXISTS uq_events_hash    ON events (event_hash, timestamp) WHERE event_hash IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_events_ts_sev       ON events (timestamp DESC, severity);


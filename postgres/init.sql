-- ═══════════════════════════════════════════════════════════════════════════
-- Partition management — auto-creates monthly partitions for the three
-- high-volume partitioned tables (events, firewall_flows, snmp_metrics).
-- Called by the processor's partition_loop every hour.
-- ═══════════════════════════════════════════════════════════════════════════
CREATE OR REPLACE FUNCTION create_monthly_partitions(
    target_table TEXT,
    months_ahead INT DEFAULT 2
) RETURNS INT AS $$
DECLARE
    start_date DATE;
    end_date   DATE;
    part_name  TEXT;
    created    INT := 0;
    i          INT;
BEGIN
    FOR i IN -1..months_ahead LOOP
        start_date := DATE_TRUNC('month', CURRENT_DATE + (i || ' months')::INTERVAL);
        end_date   := start_date + '1 month'::INTERVAL;
        part_name  := target_table || '_y' || TO_CHAR(start_date, 'YYYY') || 'm' || TO_CHAR(start_date, 'MM');

        IF NOT EXISTS (
            SELECT 1 FROM pg_class WHERE relname = part_name
        ) THEN
            EXECUTE FORMAT(
                'CREATE TABLE %I PARTITION OF %I FOR VALUES FROM (%L) TO (%L)',
                part_name, target_table, start_date, end_date
            );
            created := created + 1;
        END IF;
    END LOOP;
    RETURN created;
END;
$$ LANGUAGE plpgsql;


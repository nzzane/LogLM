CREATE TABLE IF NOT EXISTS lldp_neighbours (
    id            BIGSERIAL PRIMARY KEY,
    local_host    TEXT NOT NULL,
    local_port    TEXT,
    remote_host   TEXT,
    remote_port   TEXT,
    remote_mac    TEXT,
    remote_ip     INET,
    capabilities  TEXT[],
    last_seen     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (local_host, local_port, remote_host)
);
CREATE INDEX IF NOT EXISTS idx_lldp_local ON lldp_neighbours (local_host);

